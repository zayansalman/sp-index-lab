def load_all_data() -> tuple[pd.DataFrame, pd.Series]:
    """Load stock prices and benchmark from Parquet files.

    Returns:
        Tuple of (stock_prices DataFrame, benchmark Series).

    Raises:
        SystemExit: If required data files are empty or missing.
    """
    logger.info("Loading data from Parquet files...")

    prices_df = load_parquet("daily_prices")
    benchmark_df = load_parquet("benchmark_prices")

    if prices_df.empty:
        logger.error("daily_prices.parquet is empty or missing. Run backfill first.")
        sys.exit(1)

    if benchmark_df.empty:
        logger.error("benchmark_prices.parquet is empty or missing. Run backfill first.")
        sys.exit(1)

    # Parse dates
    prices_df["date"] = pd.to_datetime(prices_df["date"])

    # Handle both wide and long format
    if "symbol" in prices_df.columns:
        stock_prices = prices_df.pivot_table(
            index="date", columns="symbol", values="close"
        )
    else:
        stock_prices = prices_df.set_index("date")

    # Filter to the candidate pool and drop all-NaN rows
    stock_cols = [c for c in stock_prices.columns if c in CANDIDATE_POOL_TICKERS]
    stock_prices = stock_prices[stock_cols].dropna(how="all")

    # Benchmark (filter to the configured symbol — total-return index)
    if "symbol" in benchmark_df.columns:
        benchmark_df = benchmark_df[benchmark_df["symbol"] == BENCHMARK_TICKER]
    benchmark_df["date"] = pd.to_datetime(benchmark_df["date"])
    benchmark = benchmark_df.set_index("date")["close"]
    benchmark.name = "sp500"

    logger.info(
        "Loaded %d trading days, %d tickers, benchmark range %s to %s",
        len(stock_prices),
        len(stock_cols),
        stock_prices.index.min().date(),
        stock_prices.index.max().date(),
    )

    return stock_prices, benchmark


# ^IRX (13-week T-bill) is fetched but wasn't wired into Sharpe/Sortino/Jensen
# alpha — every headline used a static 4% instead of the realized rate, a
# ~15% swing on Sharpe over a period whose actual average was ~1.7%.
_FALLBACK_RISK_FREE_RATE = 0.04


def realized_risk_free_rate(start: pd.Timestamp, end: pd.Timestamp) -> float:
    """Mean realized ^IRX yield (annual, decimal) over [start, end].

    Falls back to ``_FALLBACK_RISK_FREE_RATE`` if market_indicators.parquet
    is missing/empty so a stale cache never blocks the export.
    """
    indicators = load_parquet("market_indicators")
    if indicators.empty or "risk_free" not in indicators.columns:
        logger.warning(
            "market_indicators.parquet missing 'risk_free' — falling back "
            "to a static %.1f%% risk-free rate.",
            _FALLBACK_RISK_FREE_RATE * 100,
        )
        return _FALLBACK_RISK_FREE_RATE

    indicators["date"] = pd.to_datetime(indicators["date"])
    window = indicators[(indicators["date"] >= start) & (indicators["date"] <= end)]
    rate_pct = window["risk_free"].dropna()
    if rate_pct.empty:
        return _FALLBACK_RISK_FREE_RATE

    # ^IRX quotes are annual percentage points (e.g. 3.668 == 3.668%).
    return float(rate_pct.mean()) / 100.0


