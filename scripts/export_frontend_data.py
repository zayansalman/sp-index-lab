def _load_research_block() -> dict[str, Any]:
    """Assemble the honest research-provenance block for meta.json.

    Combines the frozen dev-winner spec (race_result.json) with the
    one-shot holdout outcome (holdout_results.jsonl, latest) so the frontend
    can show the full picture — dev/holdout split, trial count, deflated
    Sharpe, and the pre-registered pass/fail — with no single crowned winner.
    Returns an empty dict if the research artefacts are absent.
    """
    research_dir = DATA_DIR / "research"
    block: dict[str, Any] = {}
    race_path = research_dir / "race_result.json"
    holdout_path = research_dir / "holdout_results.jsonl"

    if race_path.exists():
        race = json.loads(race_path.read_text())
        block["dev_winner_spec"] = {
            k: race[k] for k in ("engine", "n_policy", "overlay") if k in race
        }
        block["dev_trial_count"] = race.get("dev_trial_count")
        block["dev_end"] = race.get("dev_end")
    if holdout_path.exists():
        lines = [x for x in holdout_path.read_text().splitlines() if x.strip()]
        if lines:
            h = json.loads(lines[-1])
            block["holdout"] = {
                "window": h.get("holdout_window"),
                "passed": h.get("passed"),
                "checks": h.get("checks"),
                "deflated_sharpe": h.get("dev_deflated_sharpe"),
                "score": h.get("score"),
                "index_max_drawdown": h.get("index_max_drawdown"),
            }
            # The holdout record holds the FINAL dev trial count (after the
            # legacy re-race), which the frozen race_result predates.
            if h.get("dev_trial_count"):
                block["dev_trial_count"] = h["dev_trial_count"]
    block["retained_result"] = None  # shown side-by-side; no single headline
    return block


def export_meta(
    stock_prices: pd.DataFrame,
    benchmark: pd.Series,
    current_top50: list[str],
    headline: dict[str, Any],
) -> Path:
    """Generate meta.json with dataset metadata and headline stats.

    ``headline`` is the single source of truth for every number the
    frontend displays outside the charts (landing stats, counters) —
    components read it instead of hardcoding values that drift. The
    ``research`` block records the dev/holdout provenance so the site can
    present strategies honestly without crowning one.
    """
    logger.info("Generating meta.json...")

    display = stock_prices[stock_prices.index >= pd.Timestamp(INCEPTION_DATE)]
    start_date = display.index.min()
    end_date = display.index.max()

    meta = {
        "last_updated": str(end_date.date()),
        "inception_date": str(INCEPTION_DATE),
        "n_trading_days": len(display),
        "date_range": {
            "start": str(start_date.date()),
            "end": str(end_date.date()),
        },
        "benchmark": BENCHMARK_TICKER,
        "universe_method": "pit_membership_cap_proxy",
        "top_20_tickers": current_top50[:20],
        "top_50_tickers": current_top50,
        "headline": _clean_dict(headline),
        "research": _clean_dict(_load_research_block()),
        "generated_at": datetime.now(timezone.utc).isoformat(),
    }

    return _write_json(meta, "meta.json")


def export_concentration_curve(
    rolling: pd.DataFrame,
    stock_returns: pd.DataFrame,
    benchmark_returns: pd.Series,
    ranking_fn,
    window_days: int = 252,
) -> Path:
    """Generate concentration_curve.json from point-in-time rolling windows.

    The headline ``r_squared_at_20`` is the mean across rolling one-year
    windows of the OLS R² of benchmark returns on the top-20 *at each
    window's start* — no survivorship in the selection. The per-ticker
    cumulative curve shown in the UI is the latest window's.
    """
    logger.info("Computing concentration curve (PIT rolling windows)...")

    if rolling.empty:
        logger.warning("Rolling concentration returned empty DataFrame")
        return _write_json({"curve": [], "r_squared_at_20": None}, "concentration_curve.json")

    at20 = rolling[rolling["n_stocks"] == 20].sort_values("window_start")
    r2_at_20_mean = float(at20["r_squared"].mean())
    r2_at_20_latest = float(at20["r_squared"].iloc[-1])

    # Latest window's per-ticker cumulative curve (powers the UI).
    last_start = at20["window_start"].iloc[-1]
    window_returns = stock_returns.loc[stock_returns.index >= last_start].head(window_days)
    curve = concentration_curve(
        window_returns,
        benchmark_returns.loc[window_returns.index.min():window_returns.index.max()],
        ranked_tickers=ranking_fn(last_start),
    )

    windows = [
        {
            "window_start": str(row.window_start.date()),
            "window_end": str(row.window_end.date()),
            "r_squared_at_20": round(float(row.r_squared), 6),
        }
        for row in at20.itertuples()
    ]

    payload = {
        "curve": _df_to_records(curve),
        "r_squared_at_20": round(r2_at_20_mean, 6),
        "r_squared_at_20_latest": round(r2_at_20_latest, 6),
        "r_squared_windows": windows,
        "method": f"pit_rolling_ols_{window_days}d_step21d",
    }

    return _write_json(payload, "concentration_curve.json")


def export_variance_decomposition(rolling: pd.DataFrame) -> Path:
    """Generate variance_decomposition.json (mean R² by N across PIT windows)."""
    logger.info("Computing variance decomposition (PIT rolling mean)...")

    if rolling.empty:
        logger.warning("Rolling concentration returned empty DataFrame")
        return _write_json({"decomposition": []}, "variance_decomposition.json")

    summary = (
        rolling.groupby("n_stocks")["r_squared"]
        .agg(r_squared="mean", r_squared_min="min", r_squared_max="max")
        .reset_index()
    )

    payload = {
        "decomposition": _df_to_records(summary),
        "method": "pit_rolling_windows_mean",
    }

    return _write_json(payload, "variance_decomposition.json")


def export_performance_nav(
    stock_prices: pd.DataFrame,
    benchmark: pd.Series,
    sp20_mirror: pd.DataFrame,
    sp20_equal: pd.DataFrame,
    benchmark_nav: pd.Series,
    mirror_nav: pd.Series,
    equal_nav: pd.Series,
    alpha_nav: pd.Series | None = None,
) -> Path:
    """Generate performance_nav.json with weekly and recent daily NAV series."""
    logger.info("Building performance NAV data...")

    # --- Build combined NAV DataFrame (aligned on common dates) ---
    nav_dict: dict[str, pd.Series] = {
        "sp500": benchmark_nav,
        "sp20_mirror": mirror_nav,
        "sp20_equal": equal_nav,
    }
    if alpha_nav is not None:
        nav_dict["spn_alpha"] = alpha_nav

    all_navs = pd.DataFrame(nav_dict).dropna(how="all")
    nav_columns = list(nav_dict.keys())

    # Forward-fill interior gaps only. Truncate at the last date where EVERY
    # series still has real data — ffill past a series' end would fabricate
    # flat returns for a stale strategy (the frontend would chart performance
    # that never happened).
    last_real = min(s.last_valid_index() for s in nav_dict.values())
    all_navs = all_navs.loc[:last_real].ffill().dropna()

    # CRITICAL: re-normalise every series to $1 at the first common date.
    # Otherwise baselines (which start at inception 2014) appear visually
    # "ahead" of walk-forward strategies (which start 3 years later after
    # the training window), making comparison misleading.
    if not all_navs.empty:
        all_navs = all_navs.div(all_navs.iloc[0])

    # --- Weekly downsample: take every 5th trading day ---
    weekly_indices = list(range(0, len(all_navs), 5))
    # Always include the last data point
    if weekly_indices[-1] != len(all_navs) - 1:
        weekly_indices.append(len(all_navs) - 1)
    weekly = all_navs.iloc[weekly_indices].copy()

    weekly_records = []
    for date_val, row in weekly.iterrows():
        record = {"date": str(date_val.date())}
        for col in nav_columns:
            record[col] = _clean_value(row[col])
        weekly_records.append(record)

    # --- Recent daily: last 252 trading days ---
    recent_n = min(252, len(all_navs))
    recent = all_navs.iloc[-recent_n:].copy()

    recent_records = []
    for date_val, row in recent.iterrows():
        record = {"date": str(date_val.date())}
        for col in nav_columns:
            record[col] = _clean_value(row[col])
        recent_records.append(record)

    payload = {
        "weekly": weekly_records,
        "recent_daily": recent_records,
        "n_total_days": len(all_navs),
        "n_weekly_points": len(weekly_records),
        "n_recent_daily_points": len(recent_records),
        "indices": nav_columns,
    }

    return _write_json(payload, "performance_nav.json")


def _compute_extra_metrics(
    nav: pd.Series,
    benchmark_nav: pd.Series | None = None,
) -> dict:
    """Compute additional daily-return metrics not in compute_performance_metrics.

    Returns dict with keys: information_ratio, best_day, worst_day,
    win_rate, avg_daily_return. Deliberately does NOT return "alpha" or
    "beta" — compute_performance_metrics already provides Jensen's alpha
    and regression beta, and overriding alpha with annualised mean excess
    (as this function once did) silently changed its definition.
    """
    daily_returns = nav.pct_change().dropna()

    best_day = float(daily_returns.max()) if len(daily_returns) > 0 else 0.0
    worst_day = float(daily_returns.min()) if len(daily_returns) > 0 else 0.0
    win_rate = float((daily_returns > 0).mean()) if len(daily_returns) > 0 else 0.0
    avg_daily_return = float(daily_returns.mean()) if len(daily_returns) > 0 else 0.0

    information_ratio = 0.0

    if benchmark_nav is not None:
        bench_returns = benchmark_nav.pct_change().dropna()
        # Align on common index
        common = daily_returns.index.intersection(bench_returns.index)
        if len(common) > 10:
            excess = daily_returns.loc[common] - bench_returns.loc[common]
            excess_mean = float(excess.mean())
            excess_std = float(excess.std())
            if excess_std > 0:
                information_ratio = (excess_mean / excess_std) * (252 ** 0.5)

    return {
        "information_ratio": round(information_ratio, 6),
        "best_day": round(best_day, 6),
        "worst_day": round(worst_day, 6),
        "win_rate": round(win_rate, 6),
        "avg_daily_return": round(avg_daily_return, 8),
    }


def _window_info(nav: pd.Series) -> dict[str, Any]:
    """Describe a NAV series' window so the UI can label comparison bases."""
    return {
        "start": str(nav.index.min().date()),
        "end": str(nav.index.max().date()),
        "n_years": round(len(nav) / TRADING_DAYS_PER_YEAR, 2),
    }


def _cost_info(
    nav_gross: pd.Series | None,
    turnover: pd.Series | None,
    risk_free_rate: float = _FALLBACK_RISK_FREE_RATE,
) -> dict[str, Any]:
    """Gross-of-costs counterpart metrics (net is canonical everywhere)."""
    info: dict[str, Any] = {}
    if nav_gross is not None and len(nav_gross) > 1:
        gross = compute_performance_metrics(nav_gross, risk_free_rate=risk_free_rate)
        info["gross_cagr"] = gross.get("cagr")
        info["gross_sharpe"] = gross.get("sharpe_ratio")
    if turnover is not None and len(turnover) > 0 and nav_gross is not None:
        n_years = max(len(nav_gross) / TRADING_DAYS_PER_YEAR, 1e-9)
        info["annualized_turnover"] = float(turnover.sum()) / n_years
    return info


def export_performance_metrics(
    mirror_nav: pd.Series,
    equal_nav: pd.Series,
    benchmark_nav: pd.Series,
    alpha_nav: pd.Series | None = None,
    extras: dict[str, dict[str, Any]] | None = None,
    risk_free_rate: float = _FALLBACK_RISK_FREE_RATE,
) -> Path:
    """Generate performance_metrics.json for all indices.

    All strategy NAVs are net of transaction costs; per-strategy ``extras``
    carry the gross counterparts, turnover, and window descriptors.
    ``risk_free_rate`` should be the realized average ^IRX yield over the
    analysis window (see ``realized_risk_free_rate``), not a static guess —
    Sharpe/Sortino/Jensen alpha are all sensitive to this assumption.
    """
    logger.info("Computing performance metrics...")

    extras = extras or {}

    mirror_metrics = compute_performance_metrics(
        mirror_nav, benchmark_nav, risk_free_rate=risk_free_rate
    )
    equal_metrics = compute_performance_metrics(
        equal_nav, benchmark_nav, risk_free_rate=risk_free_rate
    )
    bench_metrics = compute_performance_metrics(
        benchmark_nav, risk_free_rate=risk_free_rate
    )

    # Enrich with extra daily-return metrics
    bench_metrics.update(_compute_extra_metrics(benchmark_nav, None))
    mirror_metrics.update(_compute_extra_metrics(mirror_nav, benchmark_nav))
    equal_metrics.update(_compute_extra_metrics(equal_nav, benchmark_nav))

    bench_metrics["window"] = _window_info(benchmark_nav)
    mirror_metrics["window"] = _window_info(mirror_nav)
    equal_metrics["window"] = _window_info(equal_nav)

    mirror_metrics.update(extras.get("sp20_mirror", {}))
    equal_metrics.update(extras.get("sp20_equal", {}))

    payload = {
        "sp500": _clean_dict(bench_metrics),
        "sp20_mirror": _clean_dict(mirror_metrics),
        "sp20_equal": _clean_dict(equal_metrics),
    }

    if alpha_nav is not None:
        alpha_metrics = compute_performance_metrics(
            alpha_nav, benchmark_nav, risk_free_rate=risk_free_rate
        )
        alpha_metrics.update(_compute_extra_metrics(alpha_nav, benchmark_nav))
        alpha_metrics["window"] = _window_info(alpha_nav)
        alpha_metrics.update(extras.get("spn_alpha", {}))
        payload["spn_alpha"] = _clean_dict(alpha_metrics)

    return _write_json(payload, "performance_metrics.json")


def export_holdings(stock_prices: pd.DataFrame, current_top20: list[str]) -> Path:
    """Generate holdings.json: current PIT top-20 with cap-proxy weights."""
    logger.info("Computing current holdings...")

    as_of = stock_prices.index.max()
    caps = rank_by_cap_proxy(load_ranking_prices(), load_shares_outstanding(), as_of)
    weights = caps.reindex(current_top20).dropna()
    weights = weights / weights.sum()
    last_prices = stock_prices.iloc[-1]

    holdings = []
    for ticker in weights.sort_values(ascending=False).index:
        holdings.append({
            "ticker": ticker,
            "name": _COMPANY_NAMES.get(ticker, ticker),
            "weight": _clean_value(weights[ticker]),
            "sector": _SECTORS.get(ticker, ""),
            "last_price": _clean_value(last_prices.get(ticker)),
        })

    payload = {
        "as_of": str(as_of.date()),
        "n_holdings": len(holdings),
        "holdings": holdings,
    }

    return _write_json(payload, "holdings.json")


def export_drawdowns(
    benchmark_nav: pd.Series,
    mirror_nav: pd.Series,
    equal_nav: pd.Series,
    alpha_nav: pd.Series | None = None,
) -> Path:
    """Generate drawdowns.json with weekly-downsampled drawdown series."""
    logger.info("Computing drawdown series...")

    # Compute drawdown from peak
    dd_dict: dict[str, pd.Series] = {
        "sp500": benchmark_nav / benchmark_nav.cummax() - 1,
        "sp20_mirror": mirror_nav / mirror_nav.cummax() - 1,
        "sp20_equal": equal_nav / equal_nav.cummax() - 1,
    }
    if alpha_nav is not None:
        dd_dict["spn_alpha"] = alpha_nav / alpha_nav.cummax() - 1

    dd_columns = list(dd_dict.keys())

    # Align on common dates; interior gaps only — truncate at the last date
    # where every series has real data (no fabricated flat tail).
    last_real = min(s.last_valid_index() for s in dd_dict.values())
    dd = pd.DataFrame(dd_dict).dropna(how="all").loc[:last_real].ffill().dropna()

    # Downsample to weekly (every 5th trading day)
    weekly_indices = list(range(0, len(dd), 5))
    if weekly_indices[-1] != len(dd) - 1:
        weekly_indices.append(len(dd) - 1)
    weekly_dd = dd.iloc[weekly_indices]

    records = []
    for date_val, row in weekly_dd.iterrows():
        record = {"date": str(date_val.date())}
        for col in dd_columns:
            record[col] = _clean_value(row[col])
        records.append(record)

    # Summary stats
    payload: dict[str, Any] = {
        "weekly": records,
        "n_points": len(records),
        "indices": dd_columns,
    }
    for col in dd_columns:
        payload[f"max_drawdown_{col}"] = _clean_value(dd[col].min())
        payload[f"max_drawdown_date_{col}"] = str(dd[col].idxmin().date())

    return _write_json(payload, "drawdowns.json")


