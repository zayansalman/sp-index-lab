def main() -> int:
    """Run all data exports and print a summary."""
    logger.info("=" * 60)
    logger.info("SP Index Lab -- Frontend Data Export")
    logger.info("=" * 60)

    # Ensure output directory exists
    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)
    logger.info("Output directory: %s", OUTPUT_DIR)

    # ------------------------------------------------------------------
    # 1. Load raw data
    # ------------------------------------------------------------------
    stock_prices, benchmark = load_all_data()

    # ------------------------------------------------------------------
    # 2. Compute derived data — point-in-time universe throughout.
    # Data before INCEPTION_DATE exists only as ranking lookback.
    # ------------------------------------------------------------------
    inception = pd.Timestamp(INCEPTION_DATE)
    # Ranking uses the dividend-unadjusted panel (loaded inside universe/
    # mirror helpers); stock_prices stays the dividend-adjusted return basis.
    universe_fn = make_universe_fn(50)

    stock_returns = stock_prices.pct_change(fill_method=None).dropna(how="all")
    benchmark_display = benchmark[benchmark.index >= inception]
    benchmark_returns = benchmark_display.pct_change().dropna()
    benchmark_nav = benchmark_display / benchmark_display.iloc[0]

    # Realized ^IRX average over the analysis window — every Sharpe/Sortino/
    # Jensen-alpha below uses this instead of an assumed rate.
    risk_free_rate = realized_risk_free_rate(
        benchmark_display.index.min(), benchmark_display.index.max()
    )
    logger.info("Realized risk-free rate (^IRX mean): %.3f%%", risk_free_rate * 100)

    logger.info("Building SP-20 Mirror (cap-weighted)...")
    sp20_mirror = build_mirror_index(
        stock_prices, top_n=20, weighting="cap",
        universe_fn=universe_fn, start=inception,
    )

    logger.info("Building SP-20 Equal (equal-weighted)...")
    sp20_equal = build_mirror_index(
        stock_prices, top_n=20, weighting="equal",
        universe_fn=universe_fn, start=inception,
    )

    def _nav_series(df: pd.DataFrame, column: str, name: str) -> pd.Series:
        return pd.Series(df[column].values, index=pd.to_datetime(df["date"]), name=name)

    mirror_nav = _nav_series(sp20_mirror, "nav", "sp20_mirror")
    equal_nav = _nav_series(sp20_equal, "nav", "sp20_equal")

    # Load SP-N Alpha NAV (net + gross, pre-computed by run_alpha_backtest.py).
    global _ALPHA_NAV_AVAILABLE
    alpha_nav = None
    alpha_nav_gross = None
    try:
        alpha_df = load_parquet("alpha_nav")
        if not alpha_df.empty:
            alpha_df["date"] = pd.to_datetime(alpha_df["date"])
            alpha_nav = pd.Series(
                alpha_df["nav"].values, index=alpha_df["date"], name="spn_alpha"
            )
            if "nav_gross" in alpha_df.columns:
                alpha_nav_gross = pd.Series(
                    alpha_df["nav_gross"].values, index=alpha_df["date"]
                )
            _ALPHA_NAV_AVAILABLE = True
            logger.info("Loaded SP-N Alpha NAV (%d days)", len(alpha_nav))
        else:
            # load_parquet returns empty (with its own warning) when the file
            # doesn't exist yet — the expected state before the first
            # run_alpha_backtest.py run. Not an error.
            logger.info("No SP-N Alpha NAV found — run scripts/run_alpha_backtest.py first")
    except Exception:
        # alpha_nav.parquet EXISTS but failed to load (corrupt file, pyarrow
        # error, missing "nav"/"date" columns from a schema change). This is
        # a genuine regression, not the benign "not yet run" case above — it
        # must be loud, or meta.json silently ships without every alpha_*
        # headline key while the job still exits 0.
        logger.exception(
            "Failed to load SP-N Alpha NAV (alpha_nav.parquet exists but is "
            "unreadable) — shipping meta.json WITHOUT alpha_* headline keys."
        )

    # Rolling point-in-time concentration (windows start at inception)
    logger.info("Computing rolling PIT concentration windows...")
    rolling = rolling_concentration(
        stock_returns.loc[stock_returns.index >= inception],
        benchmark_returns,
        ranking_fn=universe_fn,
    )

    # Gross/turnover extras per strategy (net NAVs are canonical)
    extras: dict[str, dict[str, Any]] = {
        "sp20_mirror": _cost_info(
            _nav_series(sp20_mirror, "nav_gross", "g"),
            pd.Series(sp20_mirror["turnover"].values),
            risk_free_rate=risk_free_rate,
        ),
        "sp20_equal": _cost_info(
            _nav_series(sp20_equal, "nav_gross", "g"),
            pd.Series(sp20_equal["turnover"].values),
            risk_free_rate=risk_free_rate,
        ),
    }
    if alpha_nav_gross is not None:
        extras["spn_alpha"] = _cost_info(
            alpha_nav_gross, None, risk_free_rate=risk_free_rate
        )

    # Headline block: single source of truth for numbers shown in the UI.
    current_top50 = universe_fn(stock_prices.index[-1])
    at20 = rolling[rolling["n_stocks"] == 20]
    bench_m = compute_performance_metrics(benchmark_nav, risk_free_rate=risk_free_rate)
    mirror_m = compute_performance_metrics(
        mirror_nav, benchmark_nav, risk_free_rate=risk_free_rate
    )
    equal_m = compute_performance_metrics(
        equal_nav, benchmark_nav, risk_free_rate=risk_free_rate
    )
    headline: dict[str, Any] = {
        "r_squared_at_20": float(at20["r_squared"].mean()) if not at20.empty else None,
        "sp500_cagr": bench_m.get("cagr"),
        "mirror_cagr": mirror_m.get("cagr"),
        "mirror_alpha": mirror_m.get("alpha"),
        "equal_cagr": equal_m.get("cagr"),
        "equal_alpha": equal_m.get("alpha"),
        "net_of_costs": True,
    }
    if alpha_nav is not None:
        alpha_m = compute_performance_metrics(
            alpha_nav, benchmark_nav, risk_free_rate=risk_free_rate
        )
        headline.update({
            "alpha_cagr": alpha_m.get("cagr"),
            "alpha_sharpe": alpha_m.get("sharpe_ratio"),
            "alpha_jensen": alpha_m.get("alpha"),
            "alpha_max_drawdown": alpha_m.get("max_drawdown"),
        })

    # ------------------------------------------------------------------
    # 3. Export each JSON file
    # ------------------------------------------------------------------
    written_files: list[Path] = []

    written_files.append(
        export_meta(stock_prices, benchmark, current_top50, headline)
    )
    written_files.append(
        export_concentration_curve(
            rolling,
            stock_returns.loc[stock_returns.index >= inception],
            benchmark_returns,
            ranking_fn=universe_fn,
        )
    )
    written_files.append(export_variance_decomposition(rolling))
    written_files.append(
        export_performance_nav(
            stock_prices, benchmark_display,
            sp20_mirror, sp20_equal,
            benchmark_nav, mirror_nav, equal_nav,
            alpha_nav=alpha_nav,
        )
    )
    written_files.append(
        export_performance_metrics(
            mirror_nav, equal_nav, benchmark_nav,
            alpha_nav=alpha_nav,
            extras=extras,
            risk_free_rate=risk_free_rate,
        )
    )
    written_files.append(export_holdings(stock_prices, current_top50[:20]))
    written_files.append(
        export_drawdowns(
            benchmark_nav, mirror_nav, equal_nav,
            alpha_nav=alpha_nav,
        )
    )
    written_files.append(export_strategy_holdings(stock_prices))
    written_files.append(
        export_daily_deviations(sp20_mirror, benchmark_returns)
    )

    # ------------------------------------------------------------------
    # 4. Print summary
    # ------------------------------------------------------------------
    logger.info("")
    logger.info("=" * 60)
    logger.info("Export complete. Files written:")
    logger.info("=" * 60)

    total_size = 0
    for fpath in written_files:
        size_str = _file_size_str(fpath)
        total_size += fpath.stat().st_size
        logger.info("  %-35s  %s", fpath.name, size_str)

    if total_size < 1024 * 1024:
        total_str = f"{total_size / 1024:.1f} KB"
    else:
        total_str = f"{total_size / (1024 * 1024):.1f} MB"

    logger.info("-" * 60)
    logger.info("  %-35s  %s", "TOTAL", total_str)
    logger.info("=" * 60)
    logger.info("Output directory: %s", OUTPUT_DIR)

    return 0


if __name__ == "__main__":
    sys.exit(main())
